
package dominio;

public class Pacote {
    private int codigo;
    private String nome;
    private String quantidadeCadeiras;
    private String quantidadeMesas;
    private String quantidadeCapas;
    private String quantidadeToalha;
    private String decoracao;
    private String brinquedos;

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }


    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getQuantidadeCadeiras() {
        return quantidadeCadeiras;
    }

    public void setQuantidadeCadeiras(String quantidadeCadeiras) {
        this.quantidadeCadeiras = quantidadeCadeiras;
    }

    public String getQuantidadeMesas() {
        return quantidadeMesas;
    }

    public void setQuantidadeMesas(String quantidadeMesas) {
        this.quantidadeMesas = quantidadeMesas;
    }

    public String getQuantidadeCapas() {
        return quantidadeCapas;
    }

    public void setQuantidadeCapas(String quantidadeCapas) {
        this.quantidadeCapas = quantidadeCapas;
    }

    public String getQuantidadeToalha() {
        return quantidadeToalha;
    }

    public void setQuantidadeToalha(String quantidadeToalha) {
        this.quantidadeToalha = quantidadeToalha;
    }

    public String getDecoracao() {
        return decoracao;
    }

    public void setDecoracao(String decoracao) {
        this.decoracao = decoracao;
    }

    public String getBrinquedos() {
        return brinquedos;
    }

    public void setBrinquedos(String brinquedos) {
        this.brinquedos = brinquedos;
    }

    
}
